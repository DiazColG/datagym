/**
 * Obtener todos los ejercicios (con cache)
 */
export async function obtenerEjercicios() {
    try {
        console.log('üìö Intentando cargar ejercicios de Firestore...');
        const exercisesRef = collection(db, 'exercises');
        const snapshot = await getDocs(exercisesRef);
        
        console.log('üìä Docs obtenidos de Firestore:', snapshot.size);
        
        const ejercicios = [];
        snapshot.forEach((doc) => {
            ejercicios.push({ id: doc.id, ...doc.data() });
        });
        
        // Si Firestore est√° vac√≠o, usar datos locales
        if (ejercicios.length === 0) {
            console.log('‚ö†Ô∏è Firestore vac√≠o, usando EXERCISES_DB local');
            console.log('üì¶ Ejercicios locales disponibles:', EXERCISES_DB.length);
            
            // Normalizar formato para compatibilidad
            return EXERCISES_DB.map(ej => ({
                ...ej,
                name: ej.nombre || ej.name,
                musculo: ej.grupoMuscular || ej.musculo,
                primaryMuscle: ej.grupoMuscular || ej.primaryMuscle
            }));
        }
        
        // Ordenar por popularidad y orden
        ejercicios.sort((a, b) => {
            if (b.popularidad !== a.popularidad) {
                return b.popularidad - a.popularidad;
            }
            return a.orden - b.orden;
        });
        
        console.log('‚úÖ Ejercicios cargados de Firestore:', ejercicios.length);
        return ejercicios;
    } catch (error) {
        console.error('‚ùå Error al obtener ejercicios de Firestore:', error);
        console.log('üì¶ Fallback a EXERCISES_DB local:', EXERCISES_DB.length, 'ejercicios');
        
        // Fallback a datos locales - normalizar formato
        return EXERCISES_DB.map(ej => ({
            ...ej,
            name: ej.nombre || ej.name,
            musculo: ej.grupoMuscular || ej.musculo,
            primaryMuscle: ej.grupoMuscular || ej.primaryMuscle
        }));
    }
}

